package com.android.jesse.huitao.net.aes;

/**
 * @ClassName: AesUtil
 * @Desciption: AES加解密包装工具类
 * @author: yichaohua
 * @date: 2017-12-16
 */
public class AesUtil {

    public static String encryptToString(String encryptStr) {
        if (encryptStr == null) {
            return "";
        }
        return encryptStr;
    }

    public static String decryptToString(String decryptStr) {
        if (decryptStr == null) {
            return "";
        }
        return decryptStr;
    }
}
